<h3>Why use the tracker?</h3>

<p>The Tracker was created out of a desire to, initially, simplify and speed up the
    process of recruiting new members. Over time, it evolved into a meaningful way to
    organize members into platoons and squads, as well as to ensure accurate record of
    active or inactive members.</p>

<p class="c-white"><strong>It is not a replacement for the forums. It acts as a
        complement.</strong></p>