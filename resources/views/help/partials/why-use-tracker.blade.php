<h3>Why use the tracker?</h3>

<p>The Tracker was created out of a desire to &mdash; initially &mdash; simplify and speed up the
    process of recruiting new members. Over time, it has evolved into a meaningful way to
    organize members and ensure accurate record of their activity.</p>

<p class="c-white"><strong>It is not a replacement for the forums. It acts as a
        complement.</strong></p>
<hr />
<h4>What are the benefits?</h4>

<p>Depending on how you make use of the tracker, what you stand to gain can tremendously affect the way you conduct business within your division.</p>

<ul>
    <li>Never update your division structure manually again</li>
    <li>Spend more time focusing on retention and recruiting, and less time on paperwork</li>
    <li>Always know the state of your division</li>
    <li>Make informed decisions based on data trends</li>
</ul>