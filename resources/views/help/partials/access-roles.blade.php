<h3>User Access Matrix</h3>
<hr />

<p>Policies govern all actions that can be taken on the tracker. This includes things like creating, editing, and deleting platoons and squads. The matrix below breaks down each role, and what actions are permitted.</p>

<table class="table table-striped m-t-xl">
    <thead>
    <tr>
        <th class="text-center">Role</th>
        <th class="text-center">Access</th>
    </tr>
    </thead>
    <tbody>
    <tr>
        <td class="c-accent">Member</td>
        <td>
            <ul>
                <li>View access only</li>
            </ul>
        </td>
    </tr>
    <tr>
        <td class="c-accent">Officer (new NCO)</td>
        <td>
            <ul>
                <li>Can view the division structure</li>
                <li>Can manage part-time members of a division</li>
                <li>Can manage member in-game handles</li>
            </ul>
        </td>
    </tr>
    <tr>
        <td class="c-accent">Sr Leader (SGT+)</td>
        <td>
            <ul>
                <li>Can manage (create, delete, update) any division platoons, squads, members</li>
                <li>Can remove any member (requires SGT+ forum permissions)</li>
                <li>Can change own division settings (must be CO/XO)</li>
                <li>Can give user account access (up to one below current role)</li>
                <li>Can edit division structure template</li>
            </ul>
        </td>
    </tr>
    <tr>
        <td class="c-accent">Admin (MSGT+)</td>
        <td>
            <ul>
                <li>Inherits Sr Leader Permissions</li>
                <li>Can change any division settings</li>
                <li>Admin CP</li>
            </ul>
        </td>
    </tr>
    </tbody>

</table>
