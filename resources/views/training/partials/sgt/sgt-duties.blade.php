<h4>Duties and responsibilities</h4>
<ul>
    <li>Understand point #5. Leaving a division immediately after you have been promoted is
        strongly discouraged.
    </li>
    <li>Also understand that your area of responsibility as a SGT now expands beyond that of your division. Members from
        other divisions may come to you for assistance when their leadership is not available.
        <ul>
            <li>Take care not to step on any toes or make decisions that depart from that division's norms. Utilize the
                <a href="https://www.clanaod.net/forums/showthread.php?t=79087">SGT Decision Log</a> when forced to take
                action, and document fully.
            </li>
            <li>Strive to be helpful but don't meddle or otherwise interfere.</li>
        </ul>
    </li>
</ul>

